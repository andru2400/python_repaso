import turtle

window = turtle.Screen()
turtle.title("Mi primer Turtle")
tortuga = turtle.Turtle()

tortuga.forward(50)
tortuga.right(90)     # Rotate clockwise by 90 degrees

tortuga.forward(50)
tortuga.right(90)

tortuga.forward(50)
tortuga.right(90)

tortuga.forward(50)
tortuga.right(90)

window.mainloop()